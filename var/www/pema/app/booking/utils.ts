export function isRoomSoldOut(category: string, startDate: Date) {
  const soldOutTill: Record<string, Date | null> = {
    'Pema Suite': new Date('2026-03-31'),
    'Elemental Villa': new Date('2026-03-31'),
  }

  const cutoff = soldOutTill[category]
  if (!cutoff) return false

  // Normalize start date
  const start = new Date(startDate)
  start.setHours(0, 0, 0, 0)

  // Normalize cutoff date
  const cutoffMidnight = new Date(cutoff)
  cutoffMidnight.setHours(0, 0, 0, 0)

  return start <= cutoffMidnight
}
